import { CharacterDef, Ability } from "./types";

const abilityIcon = (n: number) => `${n}`;

function makeAbility(
  id: string, name: string, desc: string,
  cd: number, mana: number, stam: number, dmg: number,
  range: number, type: Ability["type"], color: string,
  scale: number, castTime: number, knockback: number,
  opts: Partial<Ability> = {}
): Ability {
  return { id, name, description: desc, cooldown: cd, manaCost: mana, staminaCost: stam, damage: dmg, range, type, effectColor: color, effectScale: scale, castTime, knockback, icon: abilityIcon(1), ...opts };
}

